<h3 class="text-primary mb-3">Eligibility Criteria</h3>
<ul>
	<li>
		The participating entity should be an Indian registered MSMEs with atleast two years of registered
		presence in India as on March 31, 2024, and must hold a valid MSME/ Udyam certificate
	</li>
	<li>
		MSMEs participating in this category should have annual turnover of minimum INR 5 crore to INR 250 crore in the financial year</li>
	<li>
		The project / initiative must be launched, implemented and operational between April 01, 2022,
		to March 31, 2024, in the Indian markets or major part of the project / initiative falls in the
		above-mentioned period.
	</li>
	<li>
		If the project / initiative was launched before the defined period, then the participant must
		mention what was the innovation/impact during the eligibility period April 01, 2022, to March 31,
		2024
	</li>
	<li>
		Project / initiative cannot be in ideation / development stage
	</li>
	<li>
		The participating entity must have all applicable trade specific registrations example: Certificate
		of Incorporation, MSME Certificate, GST Registration Certificate, audited financials of the last 2
		years, etc.
	</li>
	<li>
		The individual applying for the Awards must be a founder, co-founder, owner, or a prominent
		leader of the business of an Indian registered MSMEs. This could include CEOs, managing
		directors, or individuals in key leadership positions in the organizations
	</li>
	<li>
		The individual must be a part of the senior leadership (MD/CEO/Founder equivalent) for a
		minimum of 2 years as on March 31, 2024
	</li>
	<li>
		The shortlisted finalist for ‘Entrepreneur of the year’ category will be determined from the
		existing set of nominations received across the industry and transformation segment. Individual
		segment entries will not be considered for this category
	</li>
	<li>
		Employees and immediate family members of the award management, sponsors and partners of
		the Awards are not allowed to participate in the Awards
	</li>
	<li>
		Participation in the Awards is subject to defined in the Terms &amp; Conditions available on website
		www.leadersoftomorrow.co.in
	</li>
	<li>
		The Award categories may be changed / modified / split / merged / increased or cancelled by the
		Awards Management and / or Jury based on the number of entries received in any category, and
		the quality of the entries. The decision in this regard will be final, non-contestable and binding on
		all participants. The Awards management will not entertain any queries in this regard.
	</li>
</ul>
